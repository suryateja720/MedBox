package com.project.medbox.databasegettersetter;

public class NewEmployee {

    private String employeeName;
    private String employeeAadhaar;
    private String employeeMobile;
    private String employeeAddress;
    private String employeeActiveStatus;

    public NewEmployee() {
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getEmployeeAadhaar() {
        return employeeAadhaar;
    }

    public void setEmployeeAadhaar(String employeeAadhaar) {
        this.employeeAadhaar = employeeAadhaar;
    }

    public String getEmployeeMobile() {
        return employeeMobile;
    }

    public void setEmployeeMobile(String employeeMobile) {
        this.employeeMobile = employeeMobile;
    }

    public String getEmployeeAddress() {
        return employeeAddress;
    }

    public void setEmployeeAddress(String employeeAddress) {
        this.employeeAddress = employeeAddress;
    }

    public String getEmployeeActiveStatus() {
        return employeeActiveStatus;
    }

    public void setEmployeeActiveStatus(String employeeActiveStatus) {
        this.employeeActiveStatus = employeeActiveStatus;
    }
}
