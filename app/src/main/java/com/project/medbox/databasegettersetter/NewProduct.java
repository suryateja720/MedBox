package com.project.medbox.databasegettersetter;

public class NewProduct {
    String productCompanyName;
    String productName;
    String productHsnCode;
    String productPack;
    String productPurchaseRate;
    String productMrp;
    String productGst;

    public NewProduct() {
    }

    public String getProductCompanyName() {
        return productCompanyName;
    }

    public void setProductCompanyName(String productCompanyName) {
        this.productCompanyName = productCompanyName;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductHsnCode() {
        return productHsnCode;
    }

    public void setProductHsnCode(String productHsnCode) {
        this.productHsnCode = productHsnCode;
    }

    public String getProductPack() {
        return productPack;
    }

    public void setProductPack(String productPack) {
        this.productPack = productPack;
    }

    public String getProductPurchaseRate() {
        return productPurchaseRate;
    }

    public void setProductPurchaseRate(String productPurchaseRate) {
        this.productPurchaseRate = productPurchaseRate;
    }

    public String getProductMrp() {
        return productMrp;
    }

    public void setProductMrp(String productMrp) {
        this.productMrp = productMrp;
    }

    public String getProductGst() {
        return productGst;
    }

    public void setProductGst(String productGst) {
        this.productGst = productGst;
    }
}
